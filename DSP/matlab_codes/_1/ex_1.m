% 清除当前图形窗口中的所有图形对象
clf;

% 定义时间序列n，从0到299
n = 0:299;

% 定义输入信号x，由两个不同频率的余弦波组成
x = cos((20/256) * pi * n) + cos((200/256) * pi * n);

% 定义第一个滤波器的分子系数
num1 = [0.5 0.27 0.77];

% 定义第二个滤波器的分子系数
num2 = [0.45 0.5 0.45];

% 定义第一个滤波器的分母系数
den1 = 1;

% 定义第二个滤波器的分母系数
den2 = [1 -0.53 0.46];

% 通过第一个滤波器处理输入信号x，得到输出信号y1
y1 = filter(num1, den1, x);

% 通过第二个滤波器处理输入信号x，得到输出信号y2
y2 = filter(num2, den2, x);

% 创建一个3行1列的子图区域，并绘制第一个子图
subplot(3, 1, 1);
% 在第一个子图中绘制输入信号x的离散图形
stem(n, x);
% 设置第一个子图的y轴标签为"输入"
ylabel('输入');

% 绘制第二个子图
subplot(3, 1, 2);
% 在第二个子图中绘制第一个滤波器输出信号y1的离散图形
stem(n, y1);
% 设置第二个子图的y轴标签为"系统1"
ylabel('系统1');

% 绘制第三个子图
subplot(3, 1, 3);
% 在第三个子图中绘制第二个滤波器输出信号y2的离散图形
stem(n, y2);
% 设置第三个子图的y轴标签为"系统2"
ylabel('系统2');

% 为所有子图添加网格线
grid;
